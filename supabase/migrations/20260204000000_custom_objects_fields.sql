-- Funktion um Custom Field Values für eine Entity zu laden
CREATE OR REPLACE FUNCTION public.get_custom_field_values(
  p_entity_type text,
  p_entity_id bigint
)
RETURNS jsonb
LANGUAGE sql
SECURITY DEFINER
STABLE
AS $$
  SELECT COALESCE(
    jsonb_object_agg(
      cfd.name,
      cfv.value
    ),
    '{}'::jsonb
  )
  FROM public.custom_field_definitions cfd
  LEFT JOIN public.custom_field_values cfv ON cfv.field_definition_id = cfd.id
    AND (
      (p_entity_type = 'contacts' AND cfv.contact_id = p_entity_id) OR
      (p_entity_type = 'companies' AND cfv.company_id = p_entity_id) OR
      (p_entity_type = 'deals' AND cfv.deal_id = p_entity_id)
    )
  WHERE cfd.entity_type = p_entity_type
    AND cfd.deleted_at IS NULL
$$;

-- Funktion um Custom Field Value zu setzen/aktualisieren
CREATE OR REPLACE FUNCTION public.set_custom_field_value(
  p_entity_type text,
  p_entity_id bigint,
  p_field_name text,
  p_value jsonb
)
RETURNS boolean
LANGUAGE plpgsql
SECURITY DEFINER
AS $$
DECLARE
  v_field_id bigint;
BEGIN
  -- Feld-ID ermitteln
  SELECT id INTO v_field_id
  FROM public.custom_field_definitions
  WHERE entity_type = p_entity_type
    AND name = p_field_name
    AND deleted_at IS NULL;
  
  IF v_field_id IS NULL THEN
    RETURN false;
  END IF;
  
  -- Upsert basierend auf Entity-Typ
  IF p_entity_type = 'contacts' THEN
    INSERT INTO public.custom_field_values (field_definition_id, contact_id, value)
    VALUES (v_field_id, p_entity_id, p_value)
    ON CONFLICT (field_definition_id, contact_id) 
    DO UPDATE SET value = p_value, updated_at = NOW();
  ELSIF p_entity_type = 'companies' THEN
    INSERT INTO public.custom_field_values (field_definition_id, company_id, value)
    VALUES (v_field_id, p_entity_id, p_value)
    ON CONFLICT (field_definition_id, company_id) 
    DO UPDATE SET value = p_value, updated_at = NOW();
  ELSIF p_entity_type = 'deals' THEN
    INSERT INTO public.custom_field_values (field_definition_id, deal_id, value)
    VALUES (v_field_id, p_entity_id, p_value)
    ON CONFLICT (field_definition_id, deal_id) 
    DO UPDATE SET value = p_value, updated_at = NOW();
  ELSE
    RETURN false;
  END IF;
  
  RETURN true;
END;
$$;

