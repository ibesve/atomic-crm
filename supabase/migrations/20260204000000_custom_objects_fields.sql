-- View für Custom Objects mit allen Feldern
CREATE OR REPLACE VIEW public.custom_objects_with_fields AS
SELECT 
  cod.id,
  cod.name,
  cod.label,
  cod.label_plural,
  cod.description,
  cod.icon,
  cod.color,
  cod.is_active,
  cod.allow_attachments,
  cod.allow_notes,
  cod.allow_tasks,
  cod.sort_order,
  cod.created_at,
  cod.updated_at,
  COALESCE(
    jsonb_agg(
      jsonb_build_object(
        'id', cfd.id,
        'name', cfd.name,
        'label', cfd.label,
        'field_type', cfd.field_type,
        'is_required', cfd.is_required,
        'options', cfd.options,
        'sort_order', cfd.sort_order
      ) ORDER BY cfd.sort_order
    ) FILTER (WHERE cfd.id IS NOT NULL),
    '[]'::jsonb
  ) as fields
FROM public.custom_object_definitions cod
LEFT JOIN public.custom_field_definitions cfd 
  ON cfd.custom_object_id = cod.id AND cfd.deleted_at IS NULL
WHERE cod.deleted_at IS NULL
GROUP BY cod.id;

