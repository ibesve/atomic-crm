-- ============================================
-- 3. Helper function: get_table_columns
-- Returns available columns for a given table/view
-- Used by the dynamic options live preview
-- ============================================

CREATE OR REPLACE FUNCTION public.get_table_columns(p_table_name text)
RETURNS TABLE(column_name text, data_type text)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
    SELECT c.column_name::text, c.data_type::text
    FROM information_schema.columns c
    WHERE c.table_schema = 'public'
      AND c.table_name = p_table_name
    ORDER BY c.ordinal_position;
$$;

-- Grant execute to authenticated users
GRANT EXECUTE ON FUNCTION public.get_table_columns(text) TO authenticated;


-- ============================================
-- 4. Helper function: get_available_tables
-- Returns list of public tables and views for the dynamic source picker
-- ============================================

CREATE OR REPLACE FUNCTION public.get_available_tables()
RETURNS TABLE(table_name text, table_type text)
LANGUAGE sql
SECURITY DEFINER
SET search_path = public
AS $$
    SELECT t.table_name::text, t.table_type::text
    FROM information_schema.tables t
    WHERE t.table_schema = 'public'
      AND t.table_type IN ('BASE TABLE', 'VIEW')
      AND t.table_name NOT LIKE 'pg_%'
      AND t.table_name NOT LIKE '_realtime%'
      AND t.table_name NOT LIKE 'schema_%'
      AND t.table_name NOT IN ('schema_migrations', 'buckets', 'objects', 's3_multipart_uploads', 's3_multipart_uploads_parts', 'hooks', 'mfa_factors', 'mfa_challenges', 'mfa_amr_claims', 'sso_providers', 'sso_domains', 'saml_providers', 'saml_relay_states', 'flow_state', 'identities', 'instances', 'sessions', 'refresh_tokens', 'audit_log_entries', 'decrypted_secrets', 'secrets')
    ORDER BY t.table_type, t.table_name;
$$;

-- Grant execute to authenticated users
GRANT EXECUTE ON FUNCTION public.get_available_tables() TO authenticated;


