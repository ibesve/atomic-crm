-- 2. Create saved_views table
-- ============================================

CREATE TABLE IF NOT EXISTS "public"."saved_views" (
    "id" bigint generated by default as identity not null,
    "created_at" timestamp with time zone default now() not null,
    "updated_at" timestamp with time zone default now() not null,
    "created_by" bigint references sales(id) on delete cascade,
    "resource" text not null,
    "name" text not null,
    "description" text,
    "filters" jsonb default '{}'::jsonb,
    "sort" jsonb,
    "columns" jsonb,
    "is_default" boolean default false not null,
    "is_shared" boolean default false not null,
    "deleted_at" timestamp with time zone,
    constraint saved_views_pkey primary key (id),
    constraint saved_views_unique_name unique (created_by, resource, name)
);

-- Enable RLS
ALTER TABLE "public"."saved_views" ENABLE ROW LEVEL SECURITY;

-- Policy: Users can see their own views + shared views
DO $$ BEGIN
CREATE POLICY "saved_views_select_policy" ON "public"."saved_views"
    FOR SELECT USING (
        auth.role() = 'authenticated' 
        AND (
            created_by = (SELECT id FROM sales WHERE auth.uid() = user_id LIMIT 1)
            OR is_shared = true
        )
        AND deleted_at IS NULL
    );
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

-- Policy: Users can insert their own views
DO $$ BEGIN
CREATE POLICY "saved_views_insert_policy" ON "public"."saved_views"
    FOR INSERT WITH CHECK (
        auth.role() = 'authenticated'
    );
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

-- Policy: Users can update their own views
DO $$ BEGIN
CREATE POLICY "saved_views_update_policy" ON "public"."saved_views"
    FOR UPDATE USING (
        auth.role() = 'authenticated'
        AND created_by = (SELECT id FROM sales WHERE auth.uid() = user_id LIMIT 1)
    );
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

-- Policy: Users can delete their own views 
DO $$ BEGIN
CREATE POLICY "saved_views_delete_policy" ON "public"."saved_views"
    FOR DELETE USING (
        auth.role() = 'authenticated'
        AND created_by = (SELECT id FROM sales WHERE auth.uid() = user_id LIMIT 1)
    );
EXCEPTION WHEN duplicate_object THEN NULL;
END $$;

-- Updated_at trigger helper
CREATE OR REPLACE FUNCTION public.update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

